	<h2>Oppdrag</h2>

	<ol>
		<li>Finne etterretning om hvor fangene befinner seg i gruvebygg X- Ray eller Zulu. Dersom dere klarer å få tak i klosser som betalingsmiddel, er dette en fordel.</li>
		<li>Finne fange 1.  Hver fange klipper i sin rubrikk på lagskort</li>
		<li>Finne fange 2</li>
		<li>Finne fange 3</li>
		<li>Finne fange 4</li>
		<li>Finne fange 5</li>
		<li>Dersom dere har klart hele rekkefølgen; starter dere på nytt igjen. </li>
	</ol>
	<p><strong>Husk:</strong> <em>Minuspoeng dersom dere blir tatt. Klosser kan bli tatt under avhør. Etteretning må ikke røpes under avhør – da blir denne verdiløs for senere bruk. Det blir gitt 0.5 p pr. fange som er blitt funnet sporadisk kontra full pott på 50 p.</em></p>
	<p><strong>Fremgangsmåte:</strong> Finne etterretning i forlegning X- ray og Zulu. Klosser som finnes er midler som kan brukes mot å bestikke evt. Politistyrker for å få tak i etterretning. Etterretning (Informasjon) kan være alt fra konkrete råd til tips til lokasjon for fanger avhengig av hvor mange klosser lagene kan betale med. Geriljaen er hverken forhandlingsvillige eller samarbeidsvillige.</p>
	<p>Skjulestedene der fange(r) holdes skjult kan skifte fra tid til tid. Geriljaen driver jevnt med fangetransport og bytte av skjulested. Den etterretningen dere får er mer trygg og korrekt jo mer dere betaler for den(større verdi). Påliteligheten til informasjonen og politiet må lagene individuelt vurdere fortløpende i forhold til egen risiko.</p>
	<p>Når fange er lokalisert skal fange klippe lagkortet på sin rubrikk: “Fange 1 klipper av for rubrikk 1”. Dersom et lag ikke makter å følge den optimale rekkefølgen: klippes det på et eget felt på kortet. Lagkortet skal alltid bæres av lagsmedlem/lagfører på kropp, og må ikke under noen omstendigheter mistes. De laminerte oppdragskortene med kart utleveres ved innsjekking og leveres inn med lagnavn i O- basen.</p>

	<strong>Poengberegning:</strong>

	<ul>
		<li>Det gis 50 p for hver komplette runde med utfrielse(Fange 1,2,3,4 og 5 i korrekt rekkefølge).</li>
		<li>Det gis kun 0.5 p pr. utfridde fange som ikke inngår i den korrekte rekkefølgen(sporadisk). Eks. hvis et lag har utfridd fange nr. 3 og 5 etter hverandre = 1 p totalt.</li>
		<li>- 10p for hver gang et lag blir tatt av gerilja</li>
		<li>- 5 p for hver gang et lag blir tatt av lokale politistyrker</li>
		<li>- 1p for Alfa Omega (utfrielses- kodeord; all avhørsvirksomhet stoppes umiddelbart)</li>
	</ul>
	Lagene må oppgi kodeord ALFA OMEGA hvis individer i laget kjenner at nå er det nok; dersom avhøret må opphøre av en eller annen grunn
