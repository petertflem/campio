<h2>Introduksjon</h2>
<p><strong>Historikk:</strong> Hendelsesforløpet foregår i Namibia hvor 5 misjonærer har blitt kidnappet og gjemt i jungelen ledet av geriljatroppen og det “Røde allianse- partiet”. Rødt allianse parti har kuppet regjeringsmakten og holder på å ta over hele landet med bruk av vold, terror og utpressing. Landets lokale politistyrker er sterkt korrupt og samarbeider tett med militsen. Fangene slaktes etter lokal skikk dersom Nattscenariotroppen ikke klarer å utfri samtlige fanger innen natten er over 03.15. Misjonærene er av norsk opprinnelse og skal etter norsk lov bringes ut av det okkuperte landet med umiddelbar virkning. Dere er spesialtrente soldater som skal inn i jungelens mørke for å utfri fanger som er blitt tatt til fange i geriljabevoktede baser i et krigsherjet område. Livene ligger i deres hender, og de som ikke blir utfridd fra geriljaens grusomme og nådeløse hender, vil bli slaktet ned som dyr før natten er omme.</p>
<p>Det enkelte lag skal utfri så mange som mulige fra de aktuelle geriljabasene innen kl. 03.15.</p>

<h2>Risiko</h2>
<p>Noen situasjoner kan virke skremmende på noen. Faktorer som: intimgrense, lukt, inntrykk, lyd og ukjente situasjoner kan oppleves helt forskjellig. Det er derfor vi har fargekoder som skal skille nivåene fra hverandre, og alle kan føle seg "trygge" på hva de kan forvente å bli utsatt for.</p>
