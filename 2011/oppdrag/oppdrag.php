<h2>Nyttig info</h2>
<p>Få i dere nok væske før, under og etter scenarioet. Gå på do rett før dere blir kjørt ut i skogen.<br>
Kle dere etter omstendighetene og været. Få i dere næring før kvelden går i gang for alvor.<br>
Det vil være et opplyst omsorgs-område(O- basen) med en "hjemmebase" for lagene. Her vil det være energi og drikke, eminente ledere som skaper god stemning og som man kan prate med. I tillegg vil det være et fristed og en hyggelig plass der alle kan komme sammen og hygge seg midt i mørket. Hele nattscenario-området vil være merket opp med tydelig/avgrenset og farget bånd. Det vil bli mulig å bytte mellom fargekodene utover scenarioet i Omsorgsbasen av ledere der.<br>
En fotograf vil være tilstede å ta bilder. Skulle det være en grunn til at offentliggjørelse av bildet på web o.l ikke skulle være greit, så må vi få melding om det i forkant.</p>
<h2>Sykepleiere/Medic</h2>
<p>To utdannede sykepleiere vil fungere som operatie medicer og har med seg alt av førstehjelpsutstyr dersom ulykken skulle være ute. De vil for det meste ha base i omsorgsbasen der man kan få et plaster på skrubbsår og lignende. De vil også tidvis patruljere på hovedveien og vil alltid være tilgjengelig på samband.</p>
