<h2>Nattscenario 2010</h2>

Nattscenario er en begivenhet Inside:Out (ungdomsarbeidet) i Oslo Kristne Senter har arrangert i en årrekke. Her skal ungdommer fra 13- 20 år i lag løse ulike oppdrag i mange spennende og utfordrende situasjoner. Dette er en opplevelse du ikke vil gå glipp av! Nattscenario er en ypperlig mulighet til å bli kjent med nye mennesker, strekke grenser og oppleve spenning på et nivå du neppe har gjort før!

    <p>I løpet av nattens mørke skal ungdommer løse oppdrag under tre vanskelighetsgrader med ulike oppgaver som laget skal løse.<br>
        Du som deltaker vil få muligheten til å velge mellom tre fargekoder(som du kommer til å være tydelig merket med); Hvit, rød og svart.<br>
        For å få være merket med svart må ha fylt 16 år eller være ferdig med ungdomskolen – og være klar for hva som helst! Rød er en middels fargekode som passer for deg som liker utfordringer – men innenfor rimelighetens grenser. Hvit derimot er en helt safe kode som gjør deg i stand til å være med på alt uten å bli ”lagt hånd på”.<br>
        Under nattscenario kommer du i situasjoner som kan kreve at du må tenke klart under forstyrrende omgivelser eller handle raskt i pressede situasjoner! Intensjonen med scenarioet, er at dere skal samarbeide og fungere optimalt som et lag for å løse oppdrag og sanke så mange poeng som mulig. Her er alle lagsmedlemmer like viktige – og det er stikkord som lagånd, samarbeid, vilje og mot som er avgjørende for å lykkes.</p>

    <p>Det tre beste lagene vil bli annonsert på weben og vil få premie på Inside:Out til høsten(nærmere info kommer ut på weben).</p>

    <p>Mer informasjon rundt oppdraget og mer beskrivelse av opplegget vil legges ut når det nærmer seg scenario.</p>

    <h2>Hvor</h2>
    Oppstart er i Inside: Out sine ungdomslokaler på OKS på Kjeller 3 juli kl. 21.30. Her blir det registrering først og introduksjon til nattens oppdrag. Deretter får lagene en bestemt tid på å legge strategi og forberede seg med kamuflering. Natten sparkes så i gang med frakt av buss og scenarioet avsluttes med tilbakekomst med buss kl 04:00 på OKS Kjeller.
