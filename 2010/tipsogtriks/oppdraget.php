<h1>Oppdraget</h1>


Hovedfokus og 1. prioritet:<br><br>

<strong>Lagene skal utfri så mange fanger som mulig i C- basene<br>
(De må lokaliseres i jungelen)</strong>

<ol>
    <li>Hvert lag må FØRST finne informasjon /etterretning om fangene i de ulike <strong>A- basene</strong>. (De må oppdages og oppsøkes i området. Agenter vil mest sannsynlig holde seg i området – de er mulige å forhandle med) Her vil et fotografi og et kodet melding av hver fange være mulig og finne. Meldingen må huskes helt til dere finner den riktige fangen og kjøper han/henne ut med klosser og den kryptiske meldingen.</li>
    <li>Deretter må lagene finne <strong>Base B</strong> som er stedet for å sanke så mange klosser som mulig. Der er det tidsbegrensing og begrensing for hvor mange fra hvert lag som kan være der inne samtidig. Stedet er IKKE nøytralt, men fiendtlig område. Dere kan risikere og bli tatt på vei inn/ ut og rundt basen. Vær observant mot fiendtlig aktivitet!</li>
    <li>Da dere husker bilde <strong>av fange + kodet melding + min. 5 klosser ;</strong> da er dere kapable for og utfri en fange i en <strong>C- base</strong>. Dere MÅ komme dere usett inn i basen, finne fangen og kjøpe han/hun ut med det dere har med dere. Er alt riktig – vil fangen klippe kortet deres og dere får 10 p. Blir dere tatt, så blir det -5 p og en grusom opplevelse…</li>
</ol>
