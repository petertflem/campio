    <h2>Historikk</h2>

    <p>Dere er spesialtrente soldater som skal inn i jungelens mørke for å utfri fanger som er blitt tatt til fange i geriljabevoktede baser i et krigsherjet område. I lang tid har det vært stammekriger i landet og uvedkommende som befant seg i landet da krigen for alvor brøt løs, har blitt tatt til fange som gisler og som "opprørere". Fangene er av ulik opprinnelse. Noen er politiske flyktninger som kjemper for demokrati og frihet, andre er kristne og andre mennesker som har blitt ofre for
        etnisk renselse. Livene ligger i deres hender, og de som ikke blir utfridd fra geriljaens grusomme og nådeløse hender, vil bli slaktet ned som dyr før natten er omme.</p>

    <h2>Oppdrag</h2>

    <p>Det enkelte lag skal utfri så mange som mulige fra de aktuelle geriljabasene innen kl. 03.00.<br>
        Klarer hele nattscenario-troppen å få ut alle fangene, vil alle liv bli spart da geriljaleiren vil bli sprengt i luften av fredsbevarende styrker kl. 04.00.</p>

    <h2>Oppdragsbeskrivelse</h2>

    Det finnes tre nivåer av oppdragsbaser merket fra A-C.<br>

    <ul>
    <li>A- base: Her skal laget skaffe seg informasjon som de må oppgi ved utfrielsen av en potensiell fange.</li>
    <li>B- Base: Her skal dere finne klossene dere skal betale med for å løse ut en fange i basen. De er gjemt overalt i huset og dere har kun 1 min på dere om gangen til å finne klossene blant mange interessante og utfordrende omgivelser.</li>
    <li>C- Base: Her kreves 5 klosser for å utfri en fange sammen med en kodet lapp fra Base A. NB: Klossene og lappen må leveres til akkurat den fangen det var avbildet av i base A. Ellers så er det ugyldig og laget må begynne på nytt. Konsekvensene er også store...</li>
    </ul>

    <p>Det vil oppholde seg et visst antall agenter i skogen som kan være til deres disposisjon eller til deres fordervelse! Tar dere sjansen? De vil forholdsvis holde seg rundt base A, men kan også bli å treffe blant jungelens trær. Dere vil kjenne dem igjen i svart og hvitt...</p>

    <h2>Intensjon</h2>

    <p>Linke ungdommen sammen til å løse oppdrag under krevende omstendigheter. Ungdommen vil bli kjent med andre og seg selv på en måte de neppe har opplevd før. Evnen til å takle stress, frustrasjon, spenning, adrenalin og utfordringer blir satt på prøve! SE verdien av dine lagkamerater og lagets evne til å løse oppdrag! Og sist men ikke minst: Ha det GØY sammen :)</p>

    <h2>Risiko</h2>

    <p>Noen situasjoner kan virke skremmende på noen. Faktorer som: intimgrense, lukt, inntrykk, lyd og ukjente situasjoner kan oppleves helt forskjellig. Det er derfor vi har fargekoder som skal skille nivåene fra hverandre, og alle kan føle seg "trygge" på hva de kan forvente å bli utsatt for.</p>

    <p>Kodeord: Alfa Omega - avhøret avsluttes UMIDDELBARTT, laget mister kun 1 p. Det er ingen skam å gi seg!</p>

    <h2>Nyttig Informasjon</h2>

    <p>Få i dere nok væske før, under og etter scenarioet. Gå på do rett før dere blir kjørt ut i skogen.<br>
        Kle dere etter omstendighetene og været. Få i dere næring før kvelden går i gang for alvor.<br>
        Det vil være et opplyst omsorgs-område med en "hjemmebase" for lagene. Her vil det være saft og boller, eminente ledere som skaper god stemning og som man kan prate med. I tillegg vil det være et fristed og en hyggelig plass der alle kan komme sammen og hygge seg midt i mørket. Hele nattscenario-området vil være merket opp med tydelig farget bånd. Basene vil være merket med en stor A, B, C. Deltakerne vil bli merket etter fargekodene: HVIT - RØD og SVART. Svart er den mest krevende og
        krever 16 års aldersgrense. Det vil bli mulig å bytte mellom fargekodene utover scenarioet i Omsorgsbasen av ledere der. Det vil bli lånt ut en Ludvigsekk til hvert lag som laget kan oppbevare klosser og evt. skift osv. Den skal leveres tilbake på OKS etter ankomst. Hvert lag vil bli utdelt et laminert skilt med grovt tegnet kart på og med poengskjema og lagnavn. Denne må IKKE mistes!!!<br>
        En fotograf vil være tilstedet å ta bilder. Skulle det være en grunn til at offentliggjørelse av bildet på web o.l ikke skulle være greit, så må vi få melding om det i forkant.</p>

    <h2>MEDIC</h2>

    <p>En utdannet sykepleier vil fungere som operativ medic og har med seg alt av førstehjelpsutstyr dersom ulykken skulle være ute. Hun vil for det meste ha base i omsorgsbasen der man kan få et plaster på skrubbsår og lignende. Hun vil også tidvis patruljere på hovedveien og vil alltid være tilgjengelig på samband.</p>

