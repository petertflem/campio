<h1>Egne og fiendlige styrker</h1>

<h2>Geriljaen- FI- styrker</h2>

<p>Geriljaen har en sterk overbevisning over at deres angrep, religion og agenda er det eneste riktige for landet. De har tatt makten i egne hender og prøver å tvinge fram autoriteten gjennom metoder som propaganda og terror mot de innfødte og gislene som er blitt tatt til fange. Geriljaen har ingen skrupler og nøler ikke med å ty til harde metoder for å trumfe gjennom. De opererer i en samlet tropp fordelt på et uvisst antall lag og patruljer. Antallet i det enkelte laget er fremdeles uvisst…
    De kan identifiseres som noe ureglementert kledd.
    De fredsbevarende styrkers tilstedeværelse anses som IKKE AKSEPTABELT og fiendtlig.</p>

<h2>Patruljepoliti – FI- styrker</h2>

<p>Patruljepolitiet er underlagt geriljabasene og patruljerer hovedsakelig alle offentlige veier. Blir du tatt ved kryssing av vei o.l er du på tynn is. De er blitt observert med kjøretøy, hund og til fots. De har hatt våpen på seg.</p>

<h2>Agenter - uvisst</h2>
<p>Et uvisst antall agenter er blitt identifisert i området. De er noe vanskelige å stole på. De kan til dels gi dere nyttig info eller hjelpemidler. De er villig til å forhandle, men er kun velvillige hvis de selv tjener på en mulig byttehandel. De vil tiltrekke seg oppmerksomhet ved et rødt lys. Observert i noe mørkt antrekk ved mørkets frembrydd. De opererer på egen hånd.</p>

<h2>Base Bravo – FI- styrker</h2>
<p>En mulig identifisert base hvor man kan hente verdifulle fargete klosser som kan kjøpe ut fanger. Et veldig risikabelt oppdrag, og stedet oppleves foreløpig som usikkert og uforutsigbart. Fienden kretser stadig rundt i området og det anbefales ikke å oppholde seg synlig og unødvendig i området. Det er tidsbegrensning på 90 sek for å være inni huset og det er begrensninger for hvor mange som kan være der inne samtidig.</p>

<h2>Omsorgsbase – egne styrker</h2>
<p>Her er det masse GOD STEMNING! Stedet er nøytralt og er oppholdssted for alle lag som trenger en pust i bakken, oppmuntring, forfriskninger og tid til å legge ny strategi. Dedikerte ledere vil være der til deres disposisjon. Medic og fotograf vil også være stasjonert der.
    <br>Mulighet for å bytte fargekode.</p>

<h2>Terreng</h2>
<p>Området består av frodig vegetasjon og til dels åpent landskap. Lite fjell og bakketopper.</p>

